package nl.papendorp.tdd.katas.beginner.fuzzyducky;

import org.junit.jupiter.api.DisplayName;

@DisplayName( "Fuzzy Ducky" )
class FuzzyDuckySuite
{
	/*
	 * (a.k.a. FizzBuzz)
	 * The theatrical warm-up game 'Fuzzy Ducky' is played by the players forming a circle and in turn simply counting
	 * up, except:
	 * - if a number is divisible by 3 (e.g. 6) or contains a 3 (e.g. 13) instead the player says "Fuzzy"
	 * - if a number is divisible by 7 (e.g. 14) or contains a 7 (e.g. 17) instead the player says "Ducky"
	 * - if both rules above apply, the player says "Fuzzy Ducky"
	 * A player is out if he fails to say the correct number or fails to substitute a candidate.
	 * (Of course, a player is also out if his answer starts with "Duzzy"...)
	 *
	 * Implement the perfect player, who, given a number, correctly returns the number, "fuzzy", "ducky" or "fuzzy ducky"
	 * respectively.
	 *
	 * Examples:
	 * 13 -> "fuzzy"
	 * 14 -> "ducky
	 * 15 -> "fuzzy"
	 * 16 -> 16
	 * 27 -> "fuzzy ducky"
	 */
}
